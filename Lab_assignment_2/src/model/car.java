package model;

/**
 *
 * @author krish
 */
public class car {
    
    private String serialNo;
    private String modelNo;
    private String availability;
    private String manufacturer;
    private int yearOfMake;
    private int noOfSeats;
    private String lastUpdate;
    private String maintenanceCertiStatus;
    private String city;
    
    
    
    // 8 attributes per car

    public String getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(String serialNo) {
        this.serialNo = serialNo;
    }

    public String getModelNo() {
        return modelNo;
    }

    public void setModelNo(String modelNo) {
        this.modelNo = modelNo;
    }

    public String getAvailability() {
        return availability;
    }

    public void setAvailability(String availability) {
        this.availability = availability;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public int getYearOfMake() {
        return yearOfMake;
    }

    public void setYearOfMake(int yearOfMake) {
        this.yearOfMake = yearOfMake;
    }

    public int getNoOfSeats() {
        return noOfSeats;
    }

    public void setNoOfSeats(int noOfSeats) {
        this.noOfSeats = noOfSeats;
    }

    public String getLastUpdate() {
        return lastUpdate;
    }

    public void setLastUpdate(String lastUpdate) {
        this.lastUpdate = lastUpdate;
    }

    public String getMaintenanceCertiStatus() {
        return maintenanceCertiStatus;
    }

    public void setMaintenanceCertiStatus(String maintenanceCertiStatus) {
        this.maintenanceCertiStatus = maintenanceCertiStatus;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
    
    @Override
    public String toString(){
        return serialNo;
    }
    

}
