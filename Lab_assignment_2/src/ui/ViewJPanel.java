    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblView.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
            return;
        }
        DefaultTableModel viewModel = (DefaultTableModel) tblView.getModel();
        car selectedCar = (car) viewModel.getValueAt(selectedRowIndex, 0); // typecasting to car class type
        // at the 0th column we have the whole object stored
       
        fleet.deleteCar(selectedCar);
        JOptionPane.showMessageDialog(this, "Car information deleted!");
        
        populateViewTable(); // refreshing table after deletion
        
        txtSerialNo.setText("");
        txtModelNo.setText("");
        txtManufacturer.setText("");
        txtYearOfMake.setText("");
        txtNoOfSeats.setText("");
        txtLastUpdate.setText("");
        txtCity.setText("");
        txtCertificateStatus.setText("");
        txtAvailability.setText("");
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblView.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }
        DefaultTableModel viewModel = (DefaultTableModel) tblView.getModel();
        car selectedCar = (car) viewModel.getValueAt(selectedRowIndex, 0);
        
        txtSerialNo.setText(selectedCar.getSerialNo());
        txtModelNo.setText(selectedCar.getModelNo());
        txtManufacturer.setText(selectedCar.getManufacturer());
        txtYearOfMake.setText(String.valueOf(selectedCar.getYearOfMake()));
        txtNoOfSeats.setText(String.valueOf(selectedCar.getNoOfSeats()));
        txtLastUpdate.setText(selectedCar.getLastUpdate());
        txtCity.setText(selectedCar.getCity());
        txtCertificateStatus.setText(selectedCar.getMaintenanceCertiStatus());
        txtAvailability.setText(selectedCar.getAvailability());
        
        
    }//GEN-LAST:event_btnViewActionPerformed

