    private void populateViewTable() {
        DefaultTableModel viewModel = (DefaultTableModel) tblView.getModel(); // typecasting into DefaultTableModel class type
        viewModel.setRowCount(0);
        
        for(car carAdded: fleet.getFleet()){
            Object[] row = new Object[4]; // one for each column
            row[0] = carAdded; // first column becomes the actual object
            row[1] = carAdded.getModelNo();
            row[2] = carAdded.getNoOfSeats();
            row[3] = carAdded.getAvailability();
            
            viewModel.addRow(row);
        }
    }
